<template>
  <div>
  <h2>Create a new location</h2>
  <b-card>
  <b-form class="location-form" @submit.prevent="onSubmit">
    <p>
      <label for="name">Location's name:</label>
      <b-form-input id="name" v-model="name" placeholder="e.g. HofbrÃ¤uhaus" required />
    </p>

    <p>
      <label for="typeOfLocation">Type of Location:</label>
      <b-form-select id="typeOfLocation" v-model="typeOfLocation">
        <option>Restaurant</option>
        <option>Bar</option>
        <option>Pub</option>
        <option>Club</option>
        <option>private</option>
      </b-form-select>
    </p>
    <p>
      <b-button class="buttonClass" type="submit" value="Submit">Submit</b-button>
    </p>
  </b-form>
  </b-card>
  </div>
</template>

<script>
import { Api } from "@/Api";

export default {
  name: "CreateLocation",
  data() {
    return {
      name: null,
      typeOfLocation: null
    };
  },
  methods: {
    onSubmit() {
      if (this.name && this.typeOfLocation) {
        let Location = {
          name: this.name,
          typeOfLocation: this.typeOfLocation
        };
        Api.post("/locations", Location).catch(error => {
          console.log(error);
        });
        alert("New Location successfully created!");
        this.name = null;
        this.typeOfLocation = null;
      } else {
        if (!this.name) this.errors.push("Name required.");
        if (!this.typeOfLocation)
          this.errors.push("Type of Location is required.");
      }
    }
  }
};
</script>
